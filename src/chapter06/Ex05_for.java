package chapter06;

public class Ex05_for {
    public static void main(String[] args) {
        for (int i = 2; i < 10; i++) {
            System.out.print((2 * i) + " ");
        }
        System.out.println();

        for (int i = 2; i < 10; i++)
            System.out.print((3 * i) + " ");
        System.out.println();

        for (int i = 2; i < 10; i++) System.out.print((4 * i) + " ");
        System.out.println();

        for (int i = 2; i < 10; i++)
            System.out.print((5 * i) + " ");
        System.out.println();
    }
}
